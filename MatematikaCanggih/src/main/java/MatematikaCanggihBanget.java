public class MatematikaCanggihBanget {
    public int pertambahanTiga(int a, int b, int c) {
        return a + b + c;
    }

    int perkalian(int a, int b) {
        return a * b;
    }

    int modulus(int a, int b) {
        return a % b;
    }
}
